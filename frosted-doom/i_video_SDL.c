//
// I_FinishUpdate
//
void I_FinishUpdate (void)
{
    static int	lasttic;
    int	    	tics;
    int	    	i;

    // draws little dots on the bottom of the screen
    if (devparm)
    {
        i = I_GetTime();
        tics = i - lasttic;
        lasttic = i;
        if (tics > 20) tics = 20;

        for (i=0 ; i<tics*2 ; i+=2)
            screens[0][ (SCREENHEIGHT-1)*SCREENWIDTH + i] = 0xff;
        for ( ; i<20*2 ; i+=2)
            screens[0][ (SCREENHEIGHT-1)*SCREENWIDTH + i] = 0x0;
    }

    /* DRAW SCREEN */
    //if (multiply == 1)
    {
        unsigned char *line_out;
        unsigned char *line_in;
        int y;

        line_in = (unsigned char *) screens[0];
        line_out = (unsigned char *) rgbsurf->pixels;

        y = SCREENHEIGHT;
        while (y--)
        {
            memcpy(line_out, line_in, rgbsurf->w);
            line_in += SCREENWIDTH;
            line_out += rgbsurf->pitch;
        }
    }

    /* LOCK SCREEN */
    if (SDL_MUSTLOCK(screen)) {
        if (SDL_LockSurface(screen) < 0) {
            return;
        }
    }

    /* BLIT to SCREEN */
    SDL_BlitSurface(rgbsurf, NULL, screen, NULL); // blit it to the screen

    /* UNLOCK SCREEN */
    if ( SDL_MUSTLOCK(screen) ) {
        SDL_UnlockSurface(screen);
    }

    SDL_UpdateWindowSurface(window);

}


